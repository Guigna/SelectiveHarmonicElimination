    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //TODO add parameter for seed in GUI
        //TODO add plot of cost evolution
        Random r;
//seed
        try {
        jSpinner3.commitEdit();
        } catch ( java.text.ParseException e ) {System.err.println("Something went wrong while reading the seed.");}
        int value = (Integer) jSpinner3.getValue();
        
        if(value==0)
             r=new Random();
        else
             r=new Random(value);
        
//angles
        try {
        jSpinner1.commitEdit();
        } catch ( java.text.ParseException e ) {System.err.println("Something went wrong while reading the number of angles.");}
        int numberOfAngles = (Integer) jSpinner1.getValue();        
        
        
        jTextArea1.setText("");
        SelectiveHarmonicElimination she = new SelectiveHarmonicElimination(r, numberOfAngles, new SimpleRandomNeighborhood(r));
        Double sol[]=she.localSearch(numberOfAngles);
        jTextArea1.append(Arrays.toString(sol));
        jTextArea1.append("\n");
        jTextArea1.append("costo: "+she.cost(sol));
        jTextArea1.append("\n");
        jTextArea1.append("THD: "+she.computeTHD(sol));
        jTextArea1.append("\n");
        
        //jPanel1.
        double[] yData = she.getBest();
        double[] xData = she.getTime();
    
        // Create Chart
        XYChart chart = QuickChart.getChart("Sample Chart", "X", "Y", "y(x)", xData, yData);
        new SwingWrapper(chart).displayChart();
    }//GEN-LAST:event_jButton1ActionPerformed

